package com.lasun.mobile.assistant.domain;

public class ShopAd {
private String id;
private String url;
private int pic;

public String getId() {
	return id;
}

public void setId(String id) {
	this.id = id;
}

public String getUrl() {
	return url;
}

public void setUrl(String url) {
	this.url = url;
}

public int getPic() {
	return pic;
}

public void setPic(int pic) {
	this.pic = pic;
}
}
